package main

func main() {
	// For  // ; -> semicolon
	// declare  logic increment

	for sanoq := 0; sanoq <= 5; sanoq++ {
		println("Sanoq: ", sanoq)
	}

	for son := 0; son >= 0; son-- {
		println("Son: ", son)
	} // tsiklga tushib qolib  ramiz tolguncha ishledi

	for number := 0; number < 10; number++ {
		println("Number: ", number)
	}
}
